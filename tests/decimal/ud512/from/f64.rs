fn test_from_f64_ok_512(#[case] n: f64, #[case] expected: UD512) {
    let d = UD512::try_from(n).unwrap();
    assert_eq!(d, expected);

    let n = f64::from_bits(n.to_bits() | (1 << 63));
    let r = UD512::try_from(n);
    assert!(r.is_err());
}
